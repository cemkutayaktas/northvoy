export const translations: Record<Lang, typeof en> = { en, tr, de };

export function t(lang: Lang, path: string): string {
  const keys = path.split(".");
  let obj: unknown = translations[lang];
  for (const k of keys) {
    if (obj && typeof obj === "object") {
      obj = (obj as Record<string, unknown>)[k];
    } else return path;
  }
  return typeof obj === "string" ? obj : path;
}

export function tOption(lang: Lang, value: string): string {
  const map = translations[lang].options as Record<string, string>;
  return map[value] ?? value;
}
